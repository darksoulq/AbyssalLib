package com.github.darksoulq.abyssallib.world.gen.feature.tree.foliage;

import com.github.darksoulq.abyssallib.common.serialization.Codec;
import com.github.darksoulq.abyssallib.common.serialization.Codecs;
import com.github.darksoulq.abyssallib.server.registry.Registries;
import com.github.darksoulq.abyssallib.world.gen.WorldGenAccess;
import com.github.darksoulq.abyssallib.world.gen.state.provider.BlockStateProvider;
import org.bukkit.Location;

import java.util.Random;

/**
 * The base class for all tree foliage generation algorithms.
 * <p>
 * A FoliagePlacer is responsible for building the leaf canopy around specific
 * attachment points generated by the associated TrunkPlacer.
 */
public abstract class FoliagePlacer {

    /**
     * Polymorphic codec for serializing and deserializing any foliage placer implementation.
     */
    public static final Codec<FoliagePlacer> CODEC = Codec.dispatch(
        FoliagePlacer.class,
        "type",
        Codecs.STRING,
        placer -> {
            String typeId = Registries.FOLIAGE_PLACERS.getId(placer.getType());
            if (typeId == null) {
                throw new IllegalStateException("Unregistered foliage placer type");
            }
            return typeId;
        },
        typeId -> {
            FoliagePlacerType<?> type = Registries.FOLIAGE_PLACERS.get(typeId);
            if (type == null) {
                return Codec.error("Unknown foliage placer type: " + typeId);
            }
            return type.codec().unchecked();
        }
    ).describe("FoliagePlacer");

    /**
     * Executes the foliage generation logic around a specific point.
     *
     * @param level           The world generation accessor.
     * @param random          The deterministic random source.
     * @param attachmentPoint The central location to build the canopy around.
     * @param foliageProvider The block state provider for the leaf material.
     * @param radius          The calculated horizontal radius for this specific foliage layer.
     */
    public abstract void placeFoliage(WorldGenAccess level, Random random, Location attachmentPoint, BlockStateProvider foliageProvider, int radius);

    /**
     * Retrieves the specific type definition for this foliage placer.
     *
     * @return The foliage placer type associated with this instance.
     */
    public abstract FoliagePlacerType<?> getType();
}